
public class EvenAndOddNumbers {

	public static void main(String[] args) {
		int i=100;
		for (int k=2;k<=i;k++){
			if ((k%2)==0){
				System.out.println("The number is even number  "+k);
			}
			else{
				System.out.println("The number is odd number  "+k);
			}
			
		}
		
		

	}

}
